from django.http import Http404
from django.shortcuts import render
from .models import Category, Word


def index(request):
    categories = Category.objects.order_by('category_name')
    context = {
        'categories': categories,
    }
    return render(request, 'wiki/base_index.html', context)


def category_words(request, category_name):
    words = Word.objects.filter(category__category_name=category_name).order_by('-count')
    if not words:
        raise Http404("Category does not exist")
    context = {
        'category': words[0].category,
        'words': words,
    }
    return render(request, 'wiki/base_words.html', context)


